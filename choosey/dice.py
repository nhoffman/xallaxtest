def draw_dice(dice_values, dice_art=DICE_ART):
    """Return an ASCII diagram of dice faces from `dice_values`.

    The string returned contains an ASCII representation of each die.
    For example, if `dice_values = [4, 1, 3, 2]` then the string
    returned looks like this:

    ┌─────────┐ ┌─────────┐ ┌─────────┐ ┌─────────┐
    │  ●   ●  │ │         │ │  ●      │ │  ●      │
    │         │ │    ●    │ │    ●    │ │         │
    │  ●   ●  │ │         │ │      ●  │ │      ●  │
    └─────────┘ └─────────┘ └─────────┘ └─────────┘
    """

    # get a list of dice drawings (each item in dice will be a list of lines)
    dice = [dice_art[val] for val in dice_values]
    nrows = len(dice[0])

    lines = []
    # for each line in the die drawings...
    for row in range(nrows):
        # collect one line from each die drawing and connect them, separated by
        # a space
        lines.append(' '.join(die[row] for die in dice))

    # join all of the lines together into a single string
    return '\n'.join(lines)


def roll_amount(times):
    roll = []
    for i in range(times):
        roll.append(random.randint(1, 6))
    return roll



